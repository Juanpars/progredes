# Crear variables y asignar valores
juan = 3
maria = 5
adan = 6

# Imprimir las variables en una sola línea, separadas por comas
print(juan, maria, adan, sep=", ")

# Crear la variable total_manzanas y sumar las manzanas de todos
total_manzanas = juan + maria + adan

#Imprimir el total de manzanas
print("Número Total de Manzanas:", total_manzanas)

#  Experimentar con nuevas variables y operaciones
# Creando nuevas variables
pedro = 4
ana = 2

# Realizando algunas operaciones aritméticas
total_nuevo = total_manzanas + pedro + ana
resta_manzanas = total_manzanas - ana
producto_manzanas = pedro * ana
division_manzanas = total_nuevo / pedro

# Imprimiendo resultados
print("Total nuevo de manzanas:", total_nuevo)
print("Restar manzanas de Ana:", resta_manzanas)
print("Producto de manzanas de Pedro y Ana:", producto_manzanas)
print("División de total nuevo entre las manzanas de Pedro:", division_manzanas)
